#!/usr/bin/env node
// build-candidate-sql.mjs — render idempotent INSERT SQL for candidate feed rows.
//
// Usage:
//   node scripts/gov-feeds/build-candidate-sql.mjs --in results/discovery.json --out docs/candidates/foo.sql
//   node scripts/gov-feeds/build-candidate-sql.mjs --in results/discovery.json --activate  # append go-live UPDATE

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { candidateToInsertSql, candidateToActivateSql } from './lib/candidates.mjs';
import { normalizeFeedRecord, validateFeedRecord } from './lib/schema.mjs';

function arg(name) {
  const i = process.argv.indexOf(name);
  return i >= 0 ? process.argv[i + 1] : undefined;
}
const has = (name) => process.argv.includes(name);

const inPath = arg('--in');
const outPath = arg('--out') || 'results/candidate-insert.sql';
if (!inPath) {
  console.error('usage: build-candidate-sql.mjs --in discovery.json [--out file.sql] [--activate]');
  process.exit(2);
}

const raw = JSON.parse(readFileSync(inPath, 'utf8'));
const list = raw.candidates || (raw.feed_id ? [raw] : []);
if (!list.length) {
  console.error('no candidates found in input');
  process.exit(2);
}

const parts = [
  '-- Generated by scripts/gov-feeds/build-candidate-sql.mjs',
  `-- source: ${inPath}`,
  `-- at: ${new Date().toISOString()}`,
  '-- SAFETY: candidates insert with active=false. Run golive separately.',
  '',
];

for (const c of list) {
  const row = normalizeFeedRecord(c);
  const errors = validateFeedRecord(row);
  if (errors.length) throw new Error(`${row.feed_id}: ${errors.join('; ')}`);
  parts.push(candidateToInsertSql(row));
  if (has('--activate')) parts.push(candidateToActivateSql(row.feed_id));
}

mkdirSync('results', { recursive: true });
writeFileSync(outPath, parts.join('\n'));
console.log(`Wrote ${list.length} candidate(s) → ${outPath}`);
