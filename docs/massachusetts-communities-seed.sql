-- massachusetts-communities-seed.sql
-- The Massachusetts (statewide metro + Cape Cod + Berkshires + Pioneer Valley +
-- Greater Boston + South Coast) per-ZIP backbone: 11 county roots + one level=zip
-- page per requested ZIP, parent_id -> county, government inherited via cascade
-- (docs/community-build-source-of-truth.md §13). Idempotent (on conflict do nothing
-- on the case-insensitive-unique slug); requires the slug column
-- (docs/communities-slug-migration.sql) and the level enum
-- (county|city|zip|neighborhood).
--
-- MODEL (citizens think in ZIP codes): the ZIP is the resident-facing PAGE; the
-- county is the government LAYER each ZIP inherits by cascading UP parent_id. Each
-- county root = the 6 canonical civic topics (word-for-word matches to the ingest
-- CANONICAL_TOPICS + the live Utah/Colorado/Michigan county rows). Every ZIP page
-- starts government_topics=[] and inherits the county's topics via cascade.
--
-- DATA PROVENANCE (§12.0 — never guess a ZIP↔county mapping): the ZIP -> city +
-- county crosswalk was generated from the `zipcodes` PyPI package v3.0.0 (bundled,
-- offline US postal database). Each ZIP page is named "<primary USPS city> (<ZIP>)".
--
-- City councils / town meetings (Boston, Worcester, Springfield, Cambridge, Lowell,
-- Brockton, Quincy, Lynn, Newton, Somerville, Framingham, …) — note MA governs via
-- town meeting / city council per municipality — are layered on LATER, each as its
-- own level=city row with a "City government (X)" topic, ONCE that place's meeting
-- source is verified + wired on the ingest side (§13.2/§13.3 — never mint a
-- subscribable council topic before its feed exists). For now every ZIP inherits its
-- county's 6 topics via cascade, which is real, subscribable value today.
--
-- No cross-county collisions: all 592 ZIPs map to exactly one Massachusetts county in
-- the crosswalk, 0 quarantined, and none was already claimed by a live community row
-- (verified pre-seed: 0 MA rows existed). County slugs carry a -ma suffix so common
-- county names (Bristol, Essex, Middlesex, Norfolk, Plymouth — all shared with other
-- states) don't collide cross-state. Note 05501/05544 (Andover IRS ZIPs) map to Essex
-- County per the crosswalk. Suffolk County has a single ZIP here (02212, a Boston P.O.
-- block); 02238 is a Cambridge P.O. block and maps to Middlesex.

