set zip_codes = (
  select array_agg(distinct z order by z)
  from (
    select unnest(c.zip_codes) z
    union
    select unnest(k.zip_codes) from public.communities k
    where k.parent_id = c.id and k.level='zip'
  ) u
)
where c.slug='suffolk-county-ma';
