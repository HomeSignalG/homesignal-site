-- illinois-communities-seed.sql
-- The Illinois (Chicago metro + collar counties + Rockford + Metro East + Champaign-Urbana)
-- per-ZIP backbone: county roots + one level=zip page per requested ZIP, parent_id ->
-- county, government inherited via cascade (docs/community-build-source-of-truth.md §13).
-- Idempotent (on conflict do nothing on the case-insensitive-unique slug); requires the
-- slug column (docs/communities-slug-migration.sql) and the level enum (county|city|zip|
-- neighborhood).
--
-- MODEL (citizens think in ZIP codes): the ZIP is the resident-facing PAGE; the county is
-- the government LAYER each ZIP inherits by cascading UP parent_id. Each county root = the 6
-- canonical civic topics (word-for-word matches to the ingest CANONICAL_TOPICS + the live
-- Utah/Colorado/Michigan/Washington county rows). Every ZIP page starts government_topics=[]
-- and inherits the county's topics via cascade.
--
-- DATA PROVENANCE (§12.0 — never guess a ZIP↔county mapping): the ZIP -> city + county
-- crosswalk was generated from the `zipcodes` PyPI package v3.0.0 (bundled, offline US
-- postal database). Each ZIP page is named "<primary USPS city> (<ZIP>)".
--
-- City councils (Chicago, Aurora, Rockford, Naperville, Joliet, Elgin, Evanston, Cicero,
-- Champaign, Urbana, …) are layered on LATER, each as its own level=city row with a
-- "City government (X)" topic, ONCE that city's meeting source is verified + wired on the
-- ingest side (§13.2/§13.3 — never mint a subscribable council topic before its feed exists).
-- For now every ZIP inherits its county's 6 topics via cascade, real subscribable value today.
--
-- No cross-county collisions: all 474 ZIPs map to exactly one Illinois county in the
-- crosswalk, and Illinois had zero community rows pre-seed (prior states are UT/CO/MI/WA).
-- County-name casing was canonicalized where the package was inconsistent (DuPage/Dupage,
-- McHenry/Mchenry) so each county has exactly one root row.
--
-- QUARANTINED (excluded, not guessed — §12.2): 60569 (Aurora) is a UNIQUE-type corporate
-- ZIP with a blank county field in the crosswalk; excluded rather than inferred. Add it
-- later if a resident-facing page is warranted (it geolocates to DuPage/Kane).

