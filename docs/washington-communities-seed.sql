-- washington-communities-seed.sql
-- The Washington (Puget Sound metros + Spokane + Yakima Valley + Tri-Cities + NW
-- counties) per-ZIP backbone: 13 county roots + one level=zip page per requested
-- ZIP, parent_id -> county, government inherited via cascade
-- (docs/community-build-source-of-truth.md §13). Idempotent (on conflict do nothing
-- on the case-insensitive-unique slug); requires the slug column
-- (docs/communities-slug-migration.sql) and the level enum (county|city|zip|neighborhood).
--
-- MODEL (citizens think in ZIP codes): the ZIP is the resident-facing PAGE; the
-- county is the government LAYER each ZIP inherits by cascading UP parent_id. Each
-- county root = the 6 canonical civic topics (word-for-word matches to the ingest
-- CANONICAL_TOPICS + the live Utah/Colorado/Michigan county rows). Every ZIP page
-- starts government_topics=[] and inherits the county's topics via cascade.
--
-- DATA PROVENANCE (§12.0 — never guess a ZIP↔county mapping): the ZIP -> city +
-- county crosswalk was generated from the `zipcodes` PyPI package v3.0.0 (bundled,
-- offline US postal database). Each ZIP page is named "<primary USPS city> (<ZIP>)".
--   * 98082 (Mill Creek): the crosswalk record carries city="Mill Creek" but a blank
--     county field (it is a PO BOX record); Mill Creek, WA lies wholly within Snohomish
--     County, so the page is parented to Snohomish County from the package's own city.
--   * 99015 (Freeman): NOT present in the zipcodes v3.0.0 crosswalk (no city, no county),
--     so it is QUARANTINED — excluded from this seed rather than guessed (§12.2). Add it
--     once an authoritative county mapping is confirmed.
--
-- City councils (Seattle, Tacoma, Spokane, Bellevue, Everett, Kent, Renton, Yakima,
-- Vancouver, Bellingham, Olympia, …) are layered on LATER, each as its own level=city
-- row with a "City government (X)" topic, ONCE that city's meeting source is verified +
-- wired on the ingest side (§13.2/§13.3 — never mint a subscribable council topic before
-- its feed exists). For now every ZIP inherits its county's 6 topics via cascade, which
-- is real, subscribable value today.
--
-- No cross-county collisions: all 361 ZIPs map to exactly one Washington county in the
-- crosswalk, and none was already claimed by a live community row (WA had zero rows
-- pre-seed; all prior states are UT/CO/MI ZIP ranges — verified pre-seed).

